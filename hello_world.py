## Write a simple few functions that demonstrate pythons math capabilities  
def add(a, b):    
   return a + b
def subtract(a, b):
    return a - b
def multiply(a, b):
    return a * b
def divide(a, b):
    return a / b
def power(a, b):
    return a ** b
def square_root(a):
    return a ** 0.5
    